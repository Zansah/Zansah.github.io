function renderCarousel() {
    const carouselContainer = document.getElementById('carousel-container');
    let carouselHTML = '';
    
    indexData.carousel.forEach((slide, index) => {
        carouselHTML += `
            <div class="carousel-slide ${index === 0 ? 'active' : ''}">
                <img src="${slide.image}" alt="${slide.alt}"/>
                <div class="carousel-content">
                    <h1>${slide.title}</h1>
                    <p>${slide.description}</p>
                    <div class="carousel-buttons">
                        <button onclick="window.location.href='${slide.button1Url}'">${slide.button1Text}</button>
                        <button onclick="window.location.href='${slide.button2Url}'">${slide.button2Text}</button>
                    </div>
                </div>
            </div>
        `;
    });
    
    carouselHTML += `
        <button class="carousel-arrow left"><i class="fa-solid fa-chevron-left"></i></button>
        <button class="carousel-arrow right"><i class="fa-solid fa-chevron-right"></i></button>
        <div class="carousel-dots">
            ${indexData.carousel.map((_, i) => 
                `<span class="dot ${i === 0 ? 'active' : ''}"></span>`
            ).join('')}
        </div>
    `;
    
    carouselContainer.innerHTML = carouselHTML;
}

